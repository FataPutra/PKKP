    private void baca_data()
    {
        try{
            stm = Con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_READ_ONLY);
            RsKec = stm.executeQuery("SELECT * FROM kecamatan ORDER BY kode_kec");
            
            ResultSetMetaData meta = RsKec.getMetaData();
            int col = meta.getColumnCount();
            int baris = 0;
            while(RsKec.next()) {
                baris = RsKec.getRow();
            }
            dataTable = new Object[baris][col];
            int x = 0;
            RsKec.beforeFirst();
            while(RsKec.next()) {
                dataTable[x][0] = RsKec.getString("kode_kec");
                dataTable[x][1] = RsKec.getString("nama_kec");
                dataTable[x][2] = RsKec.getString("nama_kab");
                dataTable[x][3] = RsKec.getInt("jumlah_desa");
                dataTable[x][4] = RsKec.getInt("jumlah_kuota");
                x++;
            }
            tblKec.setModel(new DefaultTableModel(dataTable,header));
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
