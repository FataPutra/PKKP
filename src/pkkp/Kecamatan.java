    private void dataFromDataBaseToComboBox(){
        try {
            String query = "SELECT * FROM kabupaten";
            Statement st = Con.createStatement();
            ResultSet rs = st.executeQuery(query);
            
            while (rs.next()) {                
                cmbNamaKab.addItem(rs.getString("nama_kab"));
            }
            
            rs.last();
            int jumlahdata = rs.getRow();
            rs.first();
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    
    private void setField()
    {
        int row = tblKec.getSelectedRow();
        txtKodeKec.setText((String)tblKec.getValueAt(row, 0));
        txtNamaKec.setText((String)tblKec.getValueAt(row, 1));
        cmbNamaKab.setSelectedItem((String)tblKec.getValueAt(row, 2));
        String jmlDes = Integer.toString((Integer)tblKec.getValueAt(row, 3));
        txtJmlDes.setText(jmlDes);
        String jmlKuota = Integer.toString((Integer)tblKec.getValueAt(row, 4));
        txtJmlKuota.setText(jmlKuota);
    }
    
